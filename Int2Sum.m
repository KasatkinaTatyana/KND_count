function F=Int2Sum(phi,tetta)
F=0;
global N1 N2 dx dy
%--------------------------------------------------------------------------
A1=1;
A2=1;

%--------------------------------------------------------------------------
x_c=dx*(N1-1)/2;
y_c=dy*(N2-1)/2;
r_max=(x_c^2+y_c^2)^0.5;
%--------------------------------------------------------------------------
for x1=0:dx:(N1-1)*dx
    for y1=0:dy:(N2-1)*dy
        for x2=0:dx:(N1-1)*dx
            for y2=0:dy:(N2-1)*dy
                x=abs(x1-x2);
                y=abs(y1-y2);
                %----------------------------------------------------------
%                 r1=((x1-x_c)^2+(y1-y_c)^2)^0.5;
%                 r2=((x2-x_c)^2+(y2-y_c)^2)^0.5;
%                 
%                 A1=cos(pi/2-pi/2*r1/r_max);
%                 A2=cos(pi/2-pi/2*r2/r_max);
                %----------------------------------------------------------
                F=F+A1*A2*exp(j*(x*cos(phi)+y*sin(phi))*sin(tetta))*sin(tetta);
            end
        end
    end
end